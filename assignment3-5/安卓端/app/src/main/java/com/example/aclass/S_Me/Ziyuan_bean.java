package com.example.aclass.S_Me;

public class Ziyuan_bean {
    private String title;
    private String date;

    public Ziyuan_bean(String title, String date) {
        this.title = title;
        this.date = date;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
